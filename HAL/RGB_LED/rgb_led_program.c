enu_rgbErrorState_t rgb_init(void)
{
	/************************************************************************/
	enu_rgbErrorState_t enu_l_retFunction = RGB_OK;
  str_ledsConfig_t str_l_ledOrGroup = { 0 } ;
  /************************************************************************/
  str_l_ledOrGroup.enu_ledsPort = RGB_PORT;
  str_l_ledOrGroup.enu_ledsPinOrGroup = RGB_PINS;
  str_l_ledOrGroup.enu_ledsOperatingCurrent = RGB_OPERATING_CURRENT;
  /************************************************************************/
	enu_l_retFunction |= leds_init(&str_l_ledOrGroup);
	/************************************************************************/
	return enu_l_retFunction;
	/************************************************************************/
}
